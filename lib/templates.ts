// テンプレートをカテゴリ別に取得
export function getTemplatesByCategory(category: string): Template[] {
  return textTemplates.filter((t) => t.category === category);
}

// すべてのカテゴリを取得
export function getAllCategories(): string[] {
  const categories = new Set(textTemplates.map((t) => t.category));
  return Array.from(categories);
}

// ランダムなテンプレートを取得
export function getRandomTemplate(category?: string): Template {
  const templates = category ? getTemplatesByCategory(category) : textTemplates;
  const randomIndex = Math.floor(Math.random() * templates.length);
  return templates[randomIndex];
}

// ハッシュタグを追加
export function addHashtagsToText(text: string, hashtags: string[]): string {
  if (hashtags.length === 0) return text;
  return `${text}\n\n${hashtags.join(' ')}`;
}
